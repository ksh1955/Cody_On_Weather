package com.example.myapplication;

public class SampleData {private int style;
    private String grade;

    public SampleData(int style, String grade){
        this.style = style;
        this.grade = grade;
    }

    public int getStyle()
    {
        return this.style;
    }

    public String getGrade()
    {
        return this.grade;
    }
}
